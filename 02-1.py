# 02-1 숫자형

# 정수형
a = 123
b = -178
c = 0

# 실수형
a = 1.2
b = -3.14
c = 4.24e10
d = 4.24e-10

# 8진수 16진수
a = 0o177
b = 0x8FF

# 숫자형을 활용하기 위한 연산자
# 사칙연산
a = 3
b = 4
print(a + b)
print(a * b)
print(a / b)

# x의 y제곱을 나타내는 ** 연산자
a = 3
b = 4
print(a**b)

# 나눗셈 후 나머지를 반환하는 % 연산자
print(7 % 3)
print(3 % 7)

# 나눗셈 후 몫을 반환하는 // 연산자
print(7 // 4)
